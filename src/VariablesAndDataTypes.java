public class VariablesAndDataTypes {
    public static void main(String[] args) {
        demo();
    }
    static void demo(){
        int i = 10;
        float f = 20.5f;
        double d = 20.5f;
        boolean b = true;
        char c = 'a';

        String s = "Ha Noi";
        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(c);
        System.out.println(s);
    }
}
